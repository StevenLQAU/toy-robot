/**
 * Value of each direction, defined by binary, can be calculated easily
 */
export class DirectionValue { }
DirectionValue.EAST = 0b0001;
DirectionValue.SOUTH = 0b0010;
DirectionValue.WEST = 0b0100;
DirectionValue.NORTH = 0b1000;

/**
 * Actual text of each direction
 */
export class DirectionString { }
DirectionString.EAST = 'EAST';
DirectionString.SOUTH = 'SOUTH';
DirectionString.WEST = 'WEST';
DirectionString.NORTH = 'NORTH';

/**
 * Text of turn direction
 */
export class TurnDirection { }
TurnDirection.LEFT = "LEFT";
TurnDirection.RIGHT = "RIGHT";



